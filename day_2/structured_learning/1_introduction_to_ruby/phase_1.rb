# Ruby Variables

=begin
In Ruby, a variable is a place to store values
of any type including Integer, Boolean,
String, Array, and Hashes.

Each variable has its own name which cannot
begin with a capital letter or a number and
we use the equal sign for assigning a value
to that variable.

The variable declaration does not require that
you mention a specific data type.
=end

my_var = 48

puts my_var
