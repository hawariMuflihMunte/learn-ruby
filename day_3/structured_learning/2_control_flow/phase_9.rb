# Range Ruby

=begin
A sequence of integers can be demonstrated by a range.
The range can be divided into an inclusive range where
the last integer in the sequence is included and an
exclusive range where the last integer is excluded.
=end

# Inclusive
# (3..5).each do |i|
#   puts i
# end

# Exclusive
(3...7).each do |i|
  puts i
end
